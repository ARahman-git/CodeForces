#include <bits/stdc++.h>
using namespace std;
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(NULL);

    string a, b;
    cin>>a>>b;

    ranges::transform(a, a.begin(), ::tolower);
    ranges::transform(b, b.begin(), ::tolower);

    if (a<b)
        cout<< -1;
    else if (a >b)
        cout<< 1;
    else
        cout<< 0;

    return 0;
}